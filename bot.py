def noisy_text(text: str) -> str:
    # —Ö–∞–æ—Å: —Ä–µ–≥–∏—Å—Ç—Ä, –≤—Å—Ç–∞–≤–∫–∏, –∑–∞–º–µ–Ω—ã, –æ–±—Ä—É–±–∞–Ω–∏–µ
    if random.random() < 0.3:
        text = text.lower()
    if random.random() < 0.18 and len(text) > 3:
        pos = random.randint(1, len(text)-2)
        text = text[:pos] + random.choice("–∞–±–≤–≥–¥–µ–∂–∑–∏–π–∫–ª–º–Ω–æ–ø—Ä—Å—Ç—É—Ñ—Ö—Ü—á—à—â..!!") + text[pos:]
    if random.random() < 0.12:
        text = text.replace("‚ÄºÔ∏è","!!").replace("‚ö°","*—Å—Ä–æ—á–Ω–æ*")
    if random.random() < 0.06 and len(text) > 6:
        text = text[:-random.randint(1,5)]
    # ¬´–º—É—Å–æ—Ä¬ª
    if random.random() < 0.25:
        text += "\n\nüì° –†–∞–¥–∞—Ä –ó–∞–ª–µ—Å—Å–∫–æ–π –†–µ—Å–ø—É–±–ª–∏–∫–∏"
    if random.random() < 0.1:
        text += " #—Å—Ä–æ—á–Ω–æ"
    if random.random() < 0.05:
        text = "‚ö†Ô∏è " + text
    return text

def pick_two_different(seq):
    a = random.choice(seq)
    b = random.choice(seq)
    tries = 0
    while b == a and tries < 5:
        b = random.choice(seq)
        tries += 1
    return a, b

def generate_message() -> str:
    pattern = random.choice(patterns)
    # —Ü–µ–ª–∏ ‚Äî –¢–û–õ–¨–ö–û –ó–†:
    loc1, loc2 = pick_two_different(our_locations)
